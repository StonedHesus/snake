package snake.game;

public class Point {
    /**
     *
     *
     * @Author: Andrei-Paul Ionescu
     * @Version: 0.1
     */

    // Attributes of the class.
    private final int x;
    private final int y;

    // Constructor of the class.
    public Point(int x, int y) {this.x = x; this.y = y;}

    // Getters of the class.
    public int getX() {return this.x;}
    public int getY() {return this.y;}
}
